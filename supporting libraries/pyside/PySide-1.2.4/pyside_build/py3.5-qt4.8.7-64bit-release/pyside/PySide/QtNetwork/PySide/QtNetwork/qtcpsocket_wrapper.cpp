void init_QTcpSocket(PyObject* module)
{
    SbkPySide_QtNetworkTypes[SBK_QTCPSOCKET_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QTcpSocket_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QTcpSocket", "QTcpSocket*",
        &Sbk_QTcpSocket_Type, &Shiboken::callCppDestructor< ::QTcpSocket >, (SbkObjectType*)SbkPySide_QtNetworkTypes[SBK_QABSTRACTSOCKET_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QTcpSocket_Type,
        QTcpSocket_PythonToCpp_QTcpSocket_PTR,
        is_QTcpSocket_PythonToCpp_QTcpSocket_PTR_Convertible,
        QTcpSocket_PTR_CppToPython_QTcpSocket);

    Shiboken::Conversions::registerConverterName(converter, "QTcpSocket");
    Shiboken::Conversions::registerConverterName(converter, "QTcpSocket*");
    Shiboken::Conversions::registerConverterName(converter, "QTcpSocket&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QTcpSocket).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QTcpSocketWrapper).name());


    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QTcpSocket_Type, &Sbk_QTcpSocket_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QTcpSocket_Type, &::QTcpSocket::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QTcpSocket_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QTcpSocket_Type, &::QTcpSocket::staticMetaObject, sizeof(::QTcpSocket));
}
