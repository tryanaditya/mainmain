// Python to C++ pointer conversion - returns the C++ object of the Python wrapper (keeps object identity).
static void QLabel_PythonToCpp_QLabel_PTR(PyObject* pyIn, void* cppOut) {
    Shiboken::Conversions::pythonToCppPointer(&Sbk_QLabel_Type, pyIn, cppOut);
}
static PythonToCppFunc is_QLabel_PythonToCpp_QLabel_PTR_Convertible(PyObject* pyIn) {
    if (pyIn == Py_None)
        return Shiboken::Conversions::nonePythonToCppNullPtr;
    if (PyObject_TypeCheck(pyIn, (PyTypeObject*)&Sbk_QLabel_Type))
        return QLabel_PythonToCpp_QLabel_PTR;
    return 0;
}

// C++ to Python pointer conversion - tries to find the Python wrapper for the C++ object (keeps object identity).
static PyObject* QLabel_PTR_CppToPython_QLabel(const void* cppIn) {
    return PySide::getWrapperForQObject((::QLabel*)cppIn, &Sbk_QLabel_Type);

}

void init_QLabel(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QLABEL_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QLabel_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QLabel", "QLabel*",
        &Sbk_QLabel_Type, &Shiboken::callCppDestructor< ::QLabel >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QFRAME_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QLabel_Type,
        QLabel_PythonToCpp_QLabel_PTR,
        is_QLabel_PythonToCpp_QLabel_PTR_Convertible,
        QLabel_PTR_CppToPython_QLabel);

    Shiboken::Conversions::registerConverterName(converter, "QLabel");
    Shiboken::Conversions::registerConverterName(converter, "QLabel*");
    Shiboken::Conversions::registerConverterName(converter, "QLabel&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QLabel).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QLabelWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QLabel_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QLabel_Type, &Sbk_QLabelSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QLabel_Type, &Sbk_QLabel_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QLabel_Type, &::QLabel::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QLabel_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QLabel_Type, &::QLabel::staticMetaObject, sizeof(::QLabel));
}
