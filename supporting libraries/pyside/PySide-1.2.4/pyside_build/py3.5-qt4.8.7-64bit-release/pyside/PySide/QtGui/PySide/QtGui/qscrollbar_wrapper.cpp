    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QScrollBar_Type,
        QScrollBar_PythonToCpp_QScrollBar_PTR,
        is_QScrollBar_PythonToCpp_QScrollBar_PTR_Convertible,
        QScrollBar_PTR_CppToPython_QScrollBar);

    Shiboken::Conversions::registerConverterName(converter, "QScrollBar");
    Shiboken::Conversions::registerConverterName(converter, "QScrollBar*");
    Shiboken::Conversions::registerConverterName(converter, "QScrollBar&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QScrollBar).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QScrollBarWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
