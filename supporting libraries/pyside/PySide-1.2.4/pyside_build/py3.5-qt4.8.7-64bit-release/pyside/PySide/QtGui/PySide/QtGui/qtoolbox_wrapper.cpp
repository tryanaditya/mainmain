void init_QToolBox(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QTOOLBOX_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QToolBox_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QToolBox", "QToolBox*",
        &Sbk_QToolBox_Type, &Shiboken::callCppDestructor< ::QToolBox >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QFRAME_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QToolBox_Type,
        QToolBox_PythonToCpp_QToolBox_PTR,
        is_QToolBox_PythonToCpp_QToolBox_PTR_Convertible,
        QToolBox_PTR_CppToPython_QToolBox);

    Shiboken::Conversions::registerConverterName(converter, "QToolBox");
    Shiboken::Conversions::registerConverterName(converter, "QToolBox*");
    Shiboken::Conversions::registerConverterName(converter, "QToolBox&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QToolBox).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QToolBoxWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QToolBox_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QToolBox_Type, &Sbk_QToolBoxSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QToolBox_Type, &Sbk_QToolBox_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QToolBox_Type, &::QToolBox::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QToolBox_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QToolBox_Type, &::QToolBox::staticMetaObject, sizeof(::QToolBox));
}
