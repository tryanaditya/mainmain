void init_QDialog(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QDIALOG_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QDialog_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QDialog", "QDialog*",
        &Sbk_QDialog_Type, &Shiboken::callCppDestructor< ::QDialog >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QDialog_Type,
        QDialog_PythonToCpp_QDialog_PTR,
        is_QDialog_PythonToCpp_QDialog_PTR_Convertible,
        QDialog_PTR_CppToPython_QDialog);

    Shiboken::Conversions::registerConverterName(converter, "QDialog");
    Shiboken::Conversions::registerConverterName(converter, "QDialog*");
    Shiboken::Conversions::registerConverterName(converter, "QDialog&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QDialog).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QDialogWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QDialog_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QDialog_Type, &Sbk_QDialogSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QDialog_Type, &Sbk_QDialog_typeDiscovery);

    // Initialization of enums.

    // Initialization of enum 'DialogCode'.
    SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX] = Shiboken::Enum::createScopedEnum(&Sbk_QDialog_Type,
        "DialogCode",
        "PySide.QtGui.QDialog.DialogCode",
        "QDialog::DialogCode");
    if (!SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX])
        return ;

    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX],
        &Sbk_QDialog_Type, "Rejected", (long) QDialog::Rejected))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX],
        &Sbk_QDialog_Type, "Accepted", (long) QDialog::Accepted))
        return ;
    // Register converter for enum 'QDialog::DialogCode'.
    {
        SbkConverter* converter = Shiboken::Conversions::createConverter(SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX],
            QDialog_DialogCode_CppToPython_QDialog_DialogCode);
        Shiboken::Conversions::addPythonToCppValueConversion(converter,
            QDialog_DialogCode_PythonToCpp_QDialog_DialogCode,
            is_QDialog_DialogCode_PythonToCpp_QDialog_DialogCode_Convertible);
        Shiboken::Enum::setTypeConverter(SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX], converter);
        Shiboken::Enum::setTypeConverter(SbkPySide_QtGuiTypes[SBK_QDIALOG_DIALOGCODE_IDX], converter);
        Shiboken::Conversions::registerConverterName(converter, "QDialog::DialogCode");
        Shiboken::Conversions::registerConverterName(converter, "DialogCode");
    }
    // End of 'DialogCode' enum.

    PySide::Signal::registerSignals(&Sbk_QDialog_Type, &::QDialog::staticMetaObject);

    qRegisterMetaType< ::QDialog::DialogCode >("QDialog::DialogCode");
    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QDialog_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QDialog_Type, &::QDialog::staticMetaObject, sizeof(::QDialog));
}
