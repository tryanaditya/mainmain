void init_QListWidget(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QLISTWIDGET_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QListWidget_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QListWidget", "QListWidget*",
        &Sbk_QListWidget_Type, &Shiboken::callCppDestructor< ::QListWidget >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QLISTVIEW_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QListWidget_Type,
        QListWidget_PythonToCpp_QListWidget_PTR,
        is_QListWidget_PythonToCpp_QListWidget_PTR_Convertible,
        QListWidget_PTR_CppToPython_QListWidget);

    Shiboken::Conversions::registerConverterName(converter, "QListWidget");
    Shiboken::Conversions::registerConverterName(converter, "QListWidget*");
    Shiboken::Conversions::registerConverterName(converter, "QListWidget&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QListWidget).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QListWidgetWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QListWidget_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QListWidget_Type, &Sbk_QListWidgetSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QListWidget_Type, &Sbk_QListWidget_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QListWidget_Type, &::QListWidget::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QListWidget_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QListWidget_Type, &::QListWidget::staticMetaObject, sizeof(::QListWidget));
}
