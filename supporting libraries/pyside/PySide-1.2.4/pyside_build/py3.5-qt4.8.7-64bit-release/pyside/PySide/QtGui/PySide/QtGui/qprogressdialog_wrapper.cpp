void init_QProgressDialog(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QPROGRESSDIALOG_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QProgressDialog_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QProgressDialog", "QProgressDialog*",
        &Sbk_QProgressDialog_Type, &Shiboken::callCppDestructor< ::QProgressDialog >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QDIALOG_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QProgressDialog_Type,
        QProgressDialog_PythonToCpp_QProgressDialog_PTR,
        is_QProgressDialog_PythonToCpp_QProgressDialog_PTR_Convertible,
        QProgressDialog_PTR_CppToPython_QProgressDialog);

    Shiboken::Conversions::registerConverterName(converter, "QProgressDialog");
    Shiboken::Conversions::registerConverterName(converter, "QProgressDialog*");
    Shiboken::Conversions::registerConverterName(converter, "QProgressDialog&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QProgressDialog).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QProgressDialogWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QProgressDialog_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QProgressDialog_Type, &Sbk_QProgressDialogSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QProgressDialog_Type, &Sbk_QProgressDialog_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QProgressDialog_Type, &::QProgressDialog::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QProgressDialog_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QProgressDialog_Type, &::QProgressDialog::staticMetaObject, sizeof(::QProgressDialog));
}
