void init_QCheckBox(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QCHECKBOX_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QCheckBox_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QCheckBox", "QCheckBox*",
        &Sbk_QCheckBox_Type, &Shiboken::callCppDestructor< ::QCheckBox >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QABSTRACTBUTTON_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QCheckBox_Type,
        QCheckBox_PythonToCpp_QCheckBox_PTR,
        is_QCheckBox_PythonToCpp_QCheckBox_PTR_Convertible,
        QCheckBox_PTR_CppToPython_QCheckBox);

    Shiboken::Conversions::registerConverterName(converter, "QCheckBox");
    Shiboken::Conversions::registerConverterName(converter, "QCheckBox*");
    Shiboken::Conversions::registerConverterName(converter, "QCheckBox&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QCheckBox).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QCheckBoxWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QCheckBox_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QCheckBox_Type, &Sbk_QCheckBoxSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QCheckBox_Type, &Sbk_QCheckBox_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QCheckBox_Type, &::QCheckBox::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QCheckBox_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QCheckBox_Type, &::QCheckBox::staticMetaObject, sizeof(::QCheckBox));
}
