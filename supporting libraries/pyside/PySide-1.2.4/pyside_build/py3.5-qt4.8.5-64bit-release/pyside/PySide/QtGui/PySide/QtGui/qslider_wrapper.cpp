void init_QSlider(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QSLIDER_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QSlider_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QSlider", "QSlider*",
        &Sbk_QSlider_Type, &Shiboken::callCppDestructor< ::QSlider >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QABSTRACTSLIDER_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QSlider_Type,
        QSlider_PythonToCpp_QSlider_PTR,
        is_QSlider_PythonToCpp_QSlider_PTR_Convertible,
        QSlider_PTR_CppToPython_QSlider);

    Shiboken::Conversions::registerConverterName(converter, "QSlider");
    Shiboken::Conversions::registerConverterName(converter, "QSlider*");
    Shiboken::Conversions::registerConverterName(converter, "QSlider&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QSlider).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QSliderWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QSlider_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QSlider_Type, &Sbk_QSliderSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QSlider_Type, &Sbk_QSlider_typeDiscovery);

    // Initialization of enums.

    // Initialization of enum 'TickPosition'.
    SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX] = Shiboken::Enum::createScopedEnum(&Sbk_QSlider_Type,
        "TickPosition",
        "PySide.QtGui.QSlider.TickPosition",
        "QSlider::TickPosition");
    if (!SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX])
        return ;

    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "NoTicks", (long) QSlider::NoTicks))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "TicksAbove", (long) QSlider::TicksAbove))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "TicksLeft", (long) QSlider::TicksLeft))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "TicksBelow", (long) QSlider::TicksBelow))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "TicksRight", (long) QSlider::TicksRight))
        return ;
    if (!Shiboken::Enum::createScopedEnumItem(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
        &Sbk_QSlider_Type, "TicksBothSides", (long) QSlider::TicksBothSides))
        return ;
    // Register converter for enum 'QSlider::TickPosition'.
    {
        SbkConverter* converter = Shiboken::Conversions::createConverter(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX],
            QSlider_TickPosition_CppToPython_QSlider_TickPosition);
        Shiboken::Conversions::addPythonToCppValueConversion(converter,
            QSlider_TickPosition_PythonToCpp_QSlider_TickPosition,
            is_QSlider_TickPosition_PythonToCpp_QSlider_TickPosition_Convertible);
        Shiboken::Enum::setTypeConverter(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX], converter);
        Shiboken::Enum::setTypeConverter(SbkPySide_QtGuiTypes[SBK_QSLIDER_TICKPOSITION_IDX], converter);
        Shiboken::Conversions::registerConverterName(converter, "QSlider::TickPosition");
        Shiboken::Conversions::registerConverterName(converter, "TickPosition");
    }
    // End of 'TickPosition' enum.

    PySide::Signal::registerSignals(&Sbk_QSlider_Type, &::QSlider::staticMetaObject);

    qRegisterMetaType< ::QSlider::TickPosition >("QSlider::TickPosition");
    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QSlider_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QSlider_Type, &::QSlider::staticMetaObject, sizeof(::QSlider));
}
