void init_QMenu(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QMENU_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QMenu_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QMenu", "QMenu*",
        &Sbk_QMenu_Type, &Shiboken::callCppDestructor< ::QMenu >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QMenu_Type,
        QMenu_PythonToCpp_QMenu_PTR,
        is_QMenu_PythonToCpp_QMenu_PTR_Convertible,
        QMenu_PTR_CppToPython_QMenu);

    Shiboken::Conversions::registerConverterName(converter, "QMenu");
    Shiboken::Conversions::registerConverterName(converter, "QMenu*");
    Shiboken::Conversions::registerConverterName(converter, "QMenu&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QMenu).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QMenuWrapper).name());



    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QMenu_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QMenu_Type, &Sbk_QMenuSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QMenu_Type, &Sbk_QMenu_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QMenu_Type, &::QMenu::staticMetaObject);


    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QMenu_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QMenu_Type, &::QMenu::staticMetaObject, sizeof(::QMenu));
}

