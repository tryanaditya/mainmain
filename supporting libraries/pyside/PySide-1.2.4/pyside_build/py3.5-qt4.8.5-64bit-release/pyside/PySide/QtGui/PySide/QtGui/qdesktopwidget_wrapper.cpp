void init_QDesktopWidget(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QDESKTOPWIDGET_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QDesktopWidget_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QDesktopWidget", "QDesktopWidget*",
        &Sbk_QDesktopWidget_Type, &Shiboken::callCppDestructor< ::QDesktopWidget >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QDesktopWidget_Type,
        QDesktopWidget_PythonToCpp_QDesktopWidget_PTR,
        is_QDesktopWidget_PythonToCpp_QDesktopWidget_PTR_Convertible,
        QDesktopWidget_PTR_CppToPython_QDesktopWidget);

    Shiboken::Conversions::registerConverterName(converter, "QDesktopWidget");
    Shiboken::Conversions::registerConverterName(converter, "QDesktopWidget*");
    Shiboken::Conversions::registerConverterName(converter, "QDesktopWidget&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QDesktopWidget).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QDesktopWidgetWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QDesktopWidget_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QDesktopWidget_Type, &Sbk_QDesktopWidgetSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QDesktopWidget_Type, &Sbk_QDesktopWidget_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QDesktopWidget_Type, &::QDesktopWidget::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QDesktopWidget_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QDesktopWidget_Type, &::QDesktopWidget::staticMetaObject, sizeof(::QDesktopWidget));
}
