void init_QGroupBox(PyObject* module)
{
    SbkPySide_QtGuiTypes[SBK_QGROUPBOX_IDX] = reinterpret_cast<PyTypeObject*>(&Sbk_QGroupBox_Type);

    if (!Shiboken::ObjectType::introduceWrapperType(module, "QGroupBox", "QGroupBox*",
        &Sbk_QGroupBox_Type, &Shiboken::callCppDestructor< ::QGroupBox >, (SbkObjectType*)SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX])) {
        return;
    }

    // Register Converter
    SbkConverter* converter = Shiboken::Conversions::createConverter(&Sbk_QGroupBox_Type,
        QGroupBox_PythonToCpp_QGroupBox_PTR,
        is_QGroupBox_PythonToCpp_QGroupBox_PTR_Convertible,
        QGroupBox_PTR_CppToPython_QGroupBox);

    Shiboken::Conversions::registerConverterName(converter, "QGroupBox");
    Shiboken::Conversions::registerConverterName(converter, "QGroupBox*");
    Shiboken::Conversions::registerConverterName(converter, "QGroupBox&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QGroupBox).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QGroupBoxWrapper).name());


    MultipleInheritanceInitFunction func = Shiboken::ObjectType::getMultipleIheritanceFunction(reinterpret_cast<SbkObjectType*>(SbkPySide_QtGuiTypes[SBK_QWIDGET_IDX]));
    Shiboken::ObjectType::setMultipleIheritanceFunction(&Sbk_QGroupBox_Type, func);
    Shiboken::ObjectType::setCastFunction(&Sbk_QGroupBox_Type, &Sbk_QGroupBoxSpecialCastFunction);
    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QGroupBox_Type, &Sbk_QGroupBox_typeDiscovery);

    PySide::Signal::registerSignals(&Sbk_QGroupBox_Type, &::QGroupBox::staticMetaObject);

    Shiboken::ObjectType::setSubTypeInitHook(&Sbk_QGroupBox_Type, &PySide::initQObjectSubType);
    PySide::initDynamicMetaObject(&Sbk_QGroupBox_Type, &::QGroupBox::staticMetaObject, sizeof(::QGroupBox));
}
