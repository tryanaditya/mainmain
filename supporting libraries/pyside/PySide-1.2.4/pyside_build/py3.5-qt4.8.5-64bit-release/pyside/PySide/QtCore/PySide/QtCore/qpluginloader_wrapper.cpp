    Shiboken::Conversions::registerConverterName(converter, "QPluginLoader");
    Shiboken::Conversions::registerConverterName(converter, "QPluginLoader*");
    Shiboken::Conversions::registerConverterName(converter, "QPluginLoader&");
    Shiboken::Conversions::registerConverterName(converter, typeid(::QPluginLoader).name());
    Shiboken::Conversions::registerConverterName(converter, typeid(::QPluginLoaderWrapper).name());


    Shiboken::ObjectType::setTypeDiscoveryFunctionV2(&Sbk_QPluginLoader_Type, &Sbk_QPluginLoader_typeDiscovery);

